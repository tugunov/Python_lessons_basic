# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

import random
n = int(input('Введите число элементов списка случайных чисел: '))
num_list = []
for i in range (0, n): # заполняем список n элементами
    num_list.append(random.randint(-100, 100))

print('Исходный список: {}'.format(num_list))

print('Четные делим на 4, нечетные умножаем на 2')
i = 0
for i in range(0, len(num_list)):
    if num_list[i] % 2 == 0:
        num_list[i] = num_list[i] / 4
    else:
        num_list[i] = num_list[i] * 2

print('Результирующий список: {}'.format(num_list))
