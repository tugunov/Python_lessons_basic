# Задача-2:
# Даны два произвольные списка.
# Удалите из первого списка элементы, присутствующие во втором списке.

list1 = ['a', 'b', 2, 'c', 'd', 3]
list2 = [1, 2, 3, 4, 5]

print('Первый список: {}'.format(list1))
print('Второй список: {}'.format(list2))
print('Удаляю из первого списка элементы, присутствующие во втором списке')
i = 0
while i < len(list1):
    if list1[i] in list2:
        list1.pop(i) 
        # простейший способ выкинуть элемент списка - метод pop, 
        # поэтому цикл с итерационной переменной
    else:
        i += 1 # если элемента нет во втором списке, 
               # переходим к следующему элементу

print('Итоговый список: {}'.format(list1))
